#!/bin/bash
set -euo pipefail

DEST="rtsp://207.246.68.223:8554"
LOG_DIR="/home/robiocam/logs"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
export PYTHONPATH="$SCRIPT_DIR"
CHECK_INTERVAL=2
RECONNECT_WAIT=5
MAX_FFMPEG_RETRIES=5
NETWORK_CHECK_HOST="8.8.8.8"

init_log_dir() {
  if mkdir -p "$LOG_DIR" 2>/dev/null && [[ -w "$LOG_DIR" ]]; then
    return 0
  fi
  LOG_DIR="${SCRIPT_DIR}/logs"
  mkdir -p "$LOG_DIR"
}

sanitize_camera_id() {
  local raw_id="$1"
  local sanitized
  sanitized="$(printf '%s' "$raw_id" | tr '[:space:]' '_' | sed 's/[^[:alnum:]_.-]/_/g')"
  sanitized="${sanitized#_}"
  sanitized="${sanitized%_}"
  if [[ -z "$sanitized" ]]; then
    sanitized="camera_sin_id"
  fi
  printf '%s\n' "$sanitized"
}

resolve_log_file() {
  local safe_camera_id="$1"
  local primary_log_file="${LOG_DIR}/${safe_camera_id}.log"
  local fallback_dir="${SCRIPT_DIR}/logs"
  local fallback_log_file="${fallback_dir}/${safe_camera_id}.log"
  if [[ ! -e "$primary_log_file" || -w "$primary_log_file" ]]; then
    printf '%s\n' "$primary_log_file"
    return 0
  fi
  mkdir -p "$fallback_dir"
  printf '%s\n' "$fallback_log_file"
}

wait_for_network() {
  local log_file="$1"
  local attempts=0
  while ! ping -c 1 -W 2 "$NETWORK_CHECK_HOST" &>/dev/null; do
    attempts=$((attempts + 1))
    echo "$(date '+%F %T') -> Sin red, esperando... (intento $attempts)" >> "$log_file"
    sleep "$RECONNECT_WAIT"
  done
  if [[ $attempts -gt 0 ]]; then
    echo "$(date '+%F %T') -> Red restaurada despues de $attempts intentos" >> "$log_file"
  fi
}

check_rtsp_source() {
  local src="$1"
  local host port
  host="$(echo "$src" | sed 's|rtsp://[^@]*@\([^:/]*\).*|\1|; s|rtsp://\([^:/]*\).*|\1|')"
  port="$(echo "$src" | sed 's|.*:\([0-9]*\)/.*|\1|')"
  port="${port:-554}"
  timeout 3 bash -c "echo > /dev/tcp/$host/$port" &>/dev/null
}

init_log_dir

echo "===== INICIANDO MONITOR DE CAMARAS ====="
echo "===== VEHICULO_ID: ${VEHICULO_ID:-SIN_FILTRO} ====="

declare -A CURRENT_CAMERAS
declare -A CAMERA_PIDS

fetch_camera_list() {
  python3 <<'PY'
import os, re, sys

script_dir = os.environ.get("PYTHONPATH", "")
if script_dir:
    sys.path.insert(0, script_dir)

try:
    from db.connection import db
except Exception as exc:
    print(f"ERROR: no se pudo importar db.connection: {exc}", file=sys.stderr)
    sys.exit(1)

try:
    db.open()
    vehiculo_id = os.environ.get("VEHICULO_ID", "").strip()

    if vehiculo_id:
        cameras = db.fetch_all("""
            SELECT c.* FROM camaras c
            JOIN vehiculo_camaras vc ON vc.camara_id = c.id
            WHERE vc.vehiculo_id = %s AND c.activa = true;
        """, (int(vehiculo_id),))
    else:
        cameras = db.fetch_all("SELECT * FROM camaras WHERE activa = true;")

    if not cameras:
        sys.exit(0)

    for camera in cameras:
        camera_id = camera.get("codigo_unico") or camera.get("nombre") or f"CAM{camera.get('id', '')}"
        camera_id = str(camera_id).strip()
        camera_id = re.sub(r"\s+", "_", camera_id)
        camera_id = re.sub(r"[^A-Za-z0-9_.-]", "_", camera_id).strip("_") or "camera_sin_id"
        src = camera.get("url_rtsp") or ""
        if not src:
            continue
        print("\t".join([camera_id, src]))
finally:
    try:
        db.close()
    except Exception:
        pass
PY
}

start_camera() {
  local camera_id="$1"
  local src="$2"
  local safe_camera_id log_file
  safe_camera_id="$(sanitize_camera_id "$camera_id")"
  log_file="$(resolve_log_file "$safe_camera_id")"

  echo "$(date '+%F %T') -> Iniciando ${safe_camera_id}" >> "$log_file"

  local retries=0

  while true; do

    # Esperar red antes de intentar ffmpeg
    wait_for_network "$log_file"

    # Esperar que la camara RTSP responda
    local cam_attempts=0
    while ! check_rtsp_source "$src"; do
      cam_attempts=$((cam_attempts + 1))
      echo "$(date '+%F %T') -> ${safe_camera_id}: camara no responde, esperando... (intento $cam_attempts)" >> "$log_file"
      if (( cam_attempts % 12 == 0 )); then
        wait_for_network "$log_file"
      fi
      sleep 3
    done

    echo "$(date '+%F %T') -> ${safe_camera_id}: camara accesible, lanzando ffmpeg" >> "$log_file"
    retries=0

    ffmpeg \
      -rtsp_transport tcp \
      -timeout 10000000 \
      -i "$src" \
      -c:v copy \
      -c:a libopus \
      -ar 48000 \
      -ac 2 \
      -b:a 96k \
      -f rtsp \
      -rtsp_transport tcp \
      -timeout 10000000 \
      "${DEST}/${safe_camera_id}" \
      >> "$log_file" 2>&1

    retries=$((retries + 1))
    echo "$(date '+%F %T') -> ${safe_camera_id} cayo (fallo #${retries}), reiniciando..." >> "$log_file"

    # Backoff: si falla muchas veces seguidas, espera mas
    if (( retries >= MAX_FFMPEG_RETRIES )); then
      echo "$(date '+%F %T') -> ${safe_camera_id}: $MAX_FFMPEG_RETRIES fallos seguidos, esperando 30s..." >> "$log_file"
      sleep 30
      retries=0
    else
      sleep 1
    fi

  done
}

stop_camera() {
  local camera_id="$1"
  local pid="${CAMERA_PIDS[$camera_id]:-}"
  if [[ -n "$pid" && "$pid" != "0" ]]; then
    echo "$(date '+%F %T') -> Deteniendo ${camera_id} (PID $pid)"
    kill "$pid" 2>/dev/null || true
    wait "$pid" 2>/dev/null || true
    unset CAMERA_PIDS["$camera_id"]
  fi
}

update_cameras() {
  local new_cameras camera_id src entry
  mapfile -t new_cameras < <(fetch_camera_list)

  declare -A NEW_CAMERAS_MAP
  for entry in "${new_cameras[@]}"; do
    IFS=$'\t' read -r camera_id src <<< "$entry"
    if [[ -n "$camera_id" && -n "$src" ]]; then
      NEW_CAMERAS_MAP["$camera_id"]="$src"
    fi
  done

  # Detener camaras eliminadas o cambiadas
  for camera_id in "${!CURRENT_CAMERAS[@]}"; do
    if [[ -z "${NEW_CAMERAS_MAP[$camera_id]:-}" ]]; then
      stop_camera "$camera_id"
      unset CURRENT_CAMERAS["$camera_id"]
    elif [[ "${CURRENT_CAMERAS[$camera_id]}" != "${NEW_CAMERAS_MAP[$camera_id]}" ]]; then
      stop_camera "$camera_id"
      unset CURRENT_CAMERAS["$camera_id"]
    fi
  done

  # Iniciar nuevas O reiniciar las que murieron
  for camera_id in "${!NEW_CAMERAS_MAP[@]}"; do
    local pid="${CAMERA_PIDS[$camera_id]:-}"

    # Si tiene PID registrado pero el proceso ya no existe, limpiar y relanzar
    if [[ -n "$pid" && "$pid" != "0" ]] && ! kill -0 "$pid" 2>/dev/null; then
      echo "$(date '+%F %T') -> ${camera_id}: proceso hijo muerto (PID $pid), relanzando..."
      unset CAMERA_PIDS["$camera_id"]
      unset CURRENT_CAMERAS["$camera_id"]
    fi

    if [[ -z "${CURRENT_CAMERAS[$camera_id]:-}" ]]; then
      start_camera "$camera_id" "${NEW_CAMERAS_MAP[$camera_id]}" &
      CAMERA_PIDS["$camera_id"]="$!"
      echo "${camera_id} PID ${CAMERA_PIDS[$camera_id]}"
    fi
  done

  # Actualizar mapa actual
  unset CURRENT_CAMERAS
  declare -gA CURRENT_CAMERAS=()
  for camera_id in "${!NEW_CAMERAS_MAP[@]}"; do
    CURRENT_CAMERAS["$camera_id"]="${NEW_CAMERAS_MAP[$camera_id]}"
  done
}

cleanup() {
  echo "$(date '+%F %T') -> Limpiando procesos..."
  for camera_id in "${!CAMERA_PIDS[@]}"; do
    stop_camera "$camera_id"
  done
  exit 0
}

trap cleanup SIGINT SIGTERM

# Bucle principal
while true; do
  update_cameras
  echo "$(date '+%F %T') -> Esperando ${CHECK_INTERVAL}s para proximo chequeo..."
  sleep "$CHECK_INTERVAL"
done







































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































